package login.vo;
//사원 출결관리VO
public class AttendanceVO extends PagingVO{
	private String no;
	private String userId;
	private String date;
	private String dateCnt;
	private String code;
	private String codeNm;
	private String approvalYn;	//승인여부
	private String annualCnt;	//연차수
	private String remark;		//코멘트
	
	
	public String getCodeNm() {
		return codeNm;
	}
	public void setCodeNm(String codeNm) {
		this.codeNm = codeNm;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getApprovalYn() {
		return approvalYn;
	}
	public void setApprovalYn(String approvalYn) {
		this.approvalYn = approvalYn;
	}
	public String getAnnualCnt() {
		return annualCnt;
	}
	public void setAnnualCnt(String annualCnt) {
		this.annualCnt = annualCnt;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}	
	
	public String getDateCnt() {
		return dateCnt;
	}
	public void setDateCnt(String dateCnt) {
		this.dateCnt = dateCnt;
	}
	@Override
	public String toString() {
		return "PunctualityVO [no=" + no + ", userId=" + userId + ", date=" + date + ", code=" + code + ", approvalYn="
				+ approvalYn + ", annualCnt=" + annualCnt + ", remark=" + remark + "]+\n"+ "pagingVO[start ="+this.getStart()+",current="+this.getCurrent()+",max="+this.getMax()+"]";
	}
}
